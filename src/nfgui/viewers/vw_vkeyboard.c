static gint _insert_dash(gchar *src, gchar *dest)
{
    gchar tmp_src[64];
    gchar buf[64];
    gint char_cnt = 0, i = 0, j = 0, dash_cnt = 0;
    gchar tmp_char = 0;
    
    memset(tmp_src, 0x00, sizeof(tmp_src));
    memset(buf, 0x00, sizeof(buf));
    
    strcpy(tmp_src, src);
    
    while (tmp_src[i] != '\0') 
    {
        if (tmp_src[i] == '-') {
            i++;
        }
        else
        {
            buf[j] = tmp_src[i];
            char_cnt++;

            if ((char_cnt % 5) == 0) {
                if (dash_cnt < 5) {
                    buf[j+1] = '-';
                    j++;
                    dash_cnt++;
                }
            }

            i++;
            j++;
        }
    } 

    strcpy(dest, buf);

    return 0;
}

