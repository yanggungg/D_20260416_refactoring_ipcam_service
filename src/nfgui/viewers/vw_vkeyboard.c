static gint _input_multilang_label(gint lang_idx, gchar key_ascii)
{
	gchar tot_commit_str[512];
	gchar tot_preedit_str[512];
	gchar *utf8_tmp;
    guint *ucs4_tmp;
	gint utf8_len;

	gint vklabel_cc;

    g_message("%s, %d, lang_idx:%d, ascii:%d", __FUNCTION__, __LINE__, lang_idx, key_ascii);

	if (lang_idx == 0)
	{
		ucs4_tmp = hangul_ic_flush(g_hic);		
		if (ucs4_tmp[0] != 0) {
			vklabel_cc = nfui_nfvklabel_get_cursor_cc((NFVKLABEL*)g_vklabel);
			if (vklabel_cc != g_max_ch-1) nfui_nfvklabel_set_select_state((NFVKLABEL*)g_vklabel, vklabel_cc+1);
		}
	}

	ucs4_tmp = hangul_ic_get_preedit_string(g_hic);
	if (ucs4_tmp[0] != 0) {	
		memset(tot_commit_str, 0x00, sizeof(tot_commit_str));
		vklabel_cc = nfui_nfvklabel_get_cursor_cc((NFVKLABEL*)g_vklabel);
		_get_tot_preedit_string(tot_commit_str);
		nfui_nfvklabel_set_string((NFVKLABEL*)g_vklabel, tot_commit_str);
		nfui_nfvklabel_set_select_state((NFVKLABEL*)g_vklabel, vklabel_cc);
	}
	else {
		if (nfui_nfvklabel_get_cursor_state((NFVKLABEL*)g_vklabel) == VK_SELECT) {
			utf8_len = g_utf8_strlen(nfui_nfvklabel_get_all_str((NFVKLABEL*)g_vklabel), -1);
			if (utf8_len >= g_max_ch) return -1;
		}
	}

	memset(tot_commit_str, 0x00, sizeof(tot_commit_str));
	memset(tot_preedit_str, 0x00, sizeof(tot_preedit_str));

    if (lang_idx == 0)
    {
		nfui_nfvklabel_input_character((NFVKLABEL*)g_vklabel, key_ascii);
    }
    else
    {		
        hangul_ic_process(g_hic, key_ascii);

        ucs4_tmp = hangul_ic_get_commit_string(g_hic);
		if (ucs4_tmp[0] != 0) {	
			utf8_tmp = g_ucs4_to_utf8(ucs4_tmp, -1, 0, 0, 0);
			strcpy(tot_commit_str, utf8_tmp);
			if (utf8_tmp) g_free(utf8_tmp);

			nfui_nfvklabel_input_string((NFVKLABEL*)g_vklabel, tot_commit_str);		

			utf8_len = g_utf8_strlen(nfui_nfvklabel_get_all_str((NFVKLABEL*)g_vklabel), -1);
			if (utf8_len >= g_max_ch) {
				hangul_ic_reset(g_hic);
				return 0;
			}
        }

        ucs4_tmp = hangul_ic_get_preedit_string(g_hic);
		if (ucs4_tmp[0] != 0) {	
			utf8_tmp = g_ucs4_to_utf8(ucs4_tmp, -1, 0, 0, 0);
			strcat(tot_preedit_str, utf8_tmp);
			if (utf8_tmp) g_free(utf8_tmp);

			if (nfui_nfvklabel_get_cursor_state((NFVKLABEL*)g_vklabel) == VK_SELECT) {
				vklabel_cc = nfui_nfvklabel_get_cursor_cc((NFVKLABEL*)g_vklabel);
				nfui_nfvklabel_input_string((NFVKLABEL*)g_vklabel, tot_preedit_str);
				if (vklabel_cc != g_max_ch-1) nfui_nfvklabel_set_select_state((NFVKLABEL*)g_vklabel, vklabel_cc);
			}
			else {
				nfui_nfvklabel_input_string((NFVKLABEL*)g_vklabel, tot_preedit_str);
				vklabel_cc = nfui_nfvklabel_get_cursor_cc((NFVKLABEL*)g_vklabel);
				nfui_nfvklabel_set_select_state((NFVKLABEL*)g_vklabel, vklabel_cc-1);
			}
		}
    }

    return 0;
}

